#include "../../../include/minishell.h"

int	is_quote(char c)
{
	return (c == '"' || c == '\'');
}

int	has_empty_quotes_at_start(char *str)
{
	return (str[0] == str[1] && (str[0] == '"' || str[0] == '\''));
}
